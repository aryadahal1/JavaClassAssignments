package day2Assignments;

public class Sum {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        int c = 15;
        int sum = a + b + c;
        System.out.println(" The sum of a, b, and c is " + sum);
        System.out.println(" The sum of " + a + ", " + b  + " and " + c + " is " + sum);
    }
}
